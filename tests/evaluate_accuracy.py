def evaluate():
    """Запускает оценку точности поиска на новых запросах"""
    bot = SQLBotRetriever()
    
    correct = 0
    total = 0
    results = []
    
    print("=" * 70)
    print("ОЦЕНКА ТОЧНОСТИ ПОИСКА (Accuracy@1)")
    print(f"Тестовых запросов: {len(test_queries)}")
    print("=" * 70)
    
    for query, expected_category in test_queries:
        total += 1
        result = bot.find_best_answer(query, use_context=False, threshold=0.15)
        
        if result and result.get('found'):
            actual_category = result.get('category', '')
            found_question = result.get('question', '')
            confidence = result.get('confidence', 0)
            is_correct = (actual_category == expected_category)
            
            if is_correct:
                correct += 1
                status = "✅"
            else:
                status = "❌"
            
            print(f"{status} {total:2d}. {query[:42]:42} | {expected_category:10} → {actual_category:10} | {confidence:.2%}")
        else:
            print(f"❌ {total:2d}. {query[:42]:42} | {expected_category:10} → НЕ НАЙДЕН")
    
    accuracy = correct / total * 100
    
    print("=" * 70)
    print(f"📊 Результаты:")
    print(f"   • Правильных ответов: {correct}/{total}")
    print(f"   • Точность (Accuracy@1): {accuracy:.1f}%")
    print("=" * 70)
    
    # Требование к точности
    if accuracy >= 85:
        print("✅ Требование выполнено: точность >= 85%")
    else:
        print(f"⚠️ Требование не выполнено: точность {accuracy:.1f}% < 85%")

if __name__ == "__main__":
    evaluate()